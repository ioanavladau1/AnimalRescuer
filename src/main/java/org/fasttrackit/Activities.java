package org.fasttrackit;

public class Activities {

    String name;
    String eat;

    public Activities(String name){
        this.name= name;}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEat() {
        return eat;
    }

    public void setEat(String eat) {
        this.eat = eat;
    }


    }