public class Vet {
    String name;
    String speciality;


    public void setSpeciality(String speciality) {
        this.speciality = speciality;
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSpeciality() {
        return speciality;
    }





}
